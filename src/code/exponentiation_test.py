from code.exponentiation import exponentiation

print(exponentiation(2, 1))  # 2
print(exponentiation(2, 2))  # 4
print(exponentiation(2, 3))  # 8
print(exponentiation(2, 0))  # 1
print(exponentiation(2, -2))  # 1/4
print(exponentiation(1, 2))  # 1
print(exponentiation(0, 2))  # 0
print(exponentiation(-2, 2))  # 4
print(exponentiation(-2, 3))  # -8
print(exponentiation(-2, -3))  # -1/8
print(exponentiation(0, 0))  # ERR
